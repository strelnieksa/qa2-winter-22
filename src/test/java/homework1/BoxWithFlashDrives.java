package homework1;

public class BoxWithFlashDrives {

    private float boxWidth;
    private float boxHeight;
    private float boxDepth;
    private int flashDrivesInside;
    private String flashDriveSizes;
    private String flashDriveManufacters;

    //---------------GETTERS/SETTERS----------------


    public float getBoxWidth() {
        return boxWidth;
    }

    public void setBoxWidth(float boxWidth) {
        this.boxWidth = boxWidth;
    }

    public float getBoxHeight() {
        return boxHeight;
    }

    public void setBoxHeight(float boxHeight) {
        this.boxHeight = boxHeight;
    }

    public float getBoxDepth() {
        return boxDepth;
    }

    public void setBoxDepth(float boxDepth) {
        this.boxDepth = boxDepth;
    }

    public int getFlashDrivesInside() {
        return flashDrivesInside;
    }

    public void setFlashDrivesInside(int flashDrivesInside) {
        this.flashDrivesInside = flashDrivesInside;
    }

    public String getFlashDriveSizes() {
        return flashDriveSizes;
    }

    public void setFlashDriveSizes(String flashDriveSizes) {
        this.flashDriveSizes = flashDriveSizes;
    }

    public String getFlashDriveManufacters() {
        return flashDriveManufacters;
    }

    public void setFlashDriveManufacters(String flashDriveManufacters) {
        this.flashDriveManufacters = flashDriveManufacters;
    }
}
